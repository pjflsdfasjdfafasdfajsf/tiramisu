// NOTE: Meta is expected to use the same format generated by AtlasPack:
//
// <image_path> <x_position> <y_position> <width> <height>

Atlas AtlasInit(MemAlloc *Alloc, TexHandle Tex, const char *Meta, Uint32 MetaLen)
{
    Atlas Result = {0};
    Assert(Alloc);

    if (!Meta || MetaLen == 0)
    {
        return Result;
    }

    Result.Tex = Tex;

    Uint32 LineCount = 0;
    const char *LineScan = Meta;
    const char *MetaEnd = Meta + MetaLen;
    while (LineScan < MetaEnd)
    {
        LineCount++;
        const char *NextLine = MemFindChar(LineScan, MetaEnd, '\n');

        if (!NextLine)
        {
            break;
        }

        LineScan = NextLine + 1;
    }

    if (LineCount == 0)
    {
        return Result;
    }

    Result.Entries = (AtlasEntry *)MemAllocPush(Alloc, sizeof(AtlasEntry) * LineCount);
    if (!Result.Entries)
    {
        return Result;
    }
    Result.Cap = LineCount;

    const char *Cur = Meta;
    const char *End = Meta + MetaLen;

    while (Cur < End && Result.Count < Result.Cap)
    {
        const char *LineEnd = MemFindChar(Cur, End, '\n');
        if (!LineEnd)
        {
            LineEnd = End;
        }

        const char *Space = MemFindChar(Cur, LineEnd, ' ');
        if (!Space)
        {
            MemAdvanceToNextLine(&Cur, End);
            continue;
        }

        Uint32 PathLen = (Uint32)(Space - Cur);
        const char *BaseName = GetBaseName(Cur, PathLen);
        Uint32 BaseLen = (Uint32)(Space - BaseName);

        AtlasEntry *Entry = &Result.Entries[Result.Count++];

        Uint32 CopyLen = BaseLen < (ATLAS_MAX_NAME - 1) ? BaseLen : (ATLAS_MAX_NAME - 1);
        MemCopy(Entry->Name, BaseName, CopyLen);
        MemNullTerminate(Entry->Name, ATLAS_MAX_NAME, CopyLen);

        Cur = Space;

        Entry->Src.Pos.X = (Float32)MemParseUint(&Cur);
        Entry->Src.Pos.Y = (Float32)MemParseUint(&Cur);
        Entry->Src.Size.W = (Float32)MemParseUint(&Cur);
        Entry->Src.Size.H = (Float32)MemParseUint(&Cur);

        MemAdvanceToNextLine(&Cur, End);
    }

    Result.IsValid = True;
    return Result;
}

Rect AtlasGetRect(Atlas *Atlas, const char *Name)
{
    Rect Result = {0};

    Assert(Atlas);
    Assert(Name);

    if (!Atlas->IsValid)
    {
        return Result;
    }

    Uint32 NameLen = CStrLen(Name);
    for (Uint32 I = 0; I < Atlas->Count; ++I)
    {
        AtlasEntry *Entry = &Atlas->Entries[I];

        Bool Match = True;
        for (Uint32 C = 0; C <= NameLen; ++C)
        {
            if (Entry->Name[C] != Name[C])
            {
                Match = False;
                break;
            }
        }

        if (Match)
        {
            return Entry->Src;
        }
    }

    return Result;
}
