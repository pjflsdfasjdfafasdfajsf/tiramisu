RenderBuf RenderBufInit(MemAlloc *MemAlloc, Uint32 Cap)
{
    RenderBuf Result = {};

    Assert(MemAlloc);
    Assert(Cap > 0);

    Result.Mem = (Uint8 *)MemAllocPush(MemAlloc, Cap);
    if (Result.Mem)
    {
        Result.Size = 0;
        Result.Cap = Cap;
        Result.IsValid = True;

        return Result;
    }

    return Result;
}

Void *RenderBufPush(RenderBuf *RenderBuf, Uint32 Bytes)
{
    Assert(RenderBuf);
    Assert(Bytes >= sizeof(RenderHeader)); // NOTE: Always must be room for the header.

    Uint32 Aligned = BufAlignUp(Bytes);

    Bool HasSpace = (RenderBuf->Size + Aligned) <= RenderBuf->Cap;
    Assert(HasSpace);

    if (HasSpace)
    {
        Uint32 Offset = BufAlignUp(RenderBuf->Size);
        Void *Result = RenderBuf->Mem + Offset;
        RenderBuf->Size = Offset + Aligned;

        return Result;
    }

    return 0;
}

Void RenderBufClear(RenderBuf *RenderBuf, Color Color)
{
    RenderClear *Cmd = (RenderClear *)RenderBufPush(RenderBuf, sizeof(RenderClear));

    if (Cmd)
    {
        Cmd->Header.Type = RenderCommand_Clear;
        Cmd->Header.Size = BufAlignUp(sizeof(RenderClear));

        Cmd->Color = Color;
    }
}

Void RenderBufDrawDebugText(RenderBuf *RenderBuf, Color Color, V2 Pos, V2 Scale, const char *Str, Uint32 StrLen)
{
    if (!Str || StrLen == 0)
    {
        return;
    }

    Uint32 StrCap = StrLen + 1;
    Uint32 Size = sizeof(RenderDrawDebugText) + StrCap;
    RenderDrawDebugText *Cmd = (RenderDrawDebugText *)RenderBufPush(RenderBuf, Size);

    if (Cmd)
    {
        Cmd->Header.Type = RenderCommand_DrawDebugText;
        Cmd->Header.Size = BufAlignUp(Size);

        Cmd->Pos = Pos;
        Cmd->Color = Color;
        Cmd->Scale = Scale;

        MemCopy(Cmd->Str, Str, StrLen);
        MemNullTerminate(Cmd->Str, StrCap, StrLen);
    }
}

Void RenderBufDrawCStr(RenderBuf *RenderBuf, Color Color, V2 Pos, V2 Scale, const char *Str)
{
    RenderBufDrawDebugText(RenderBuf, Color, Pos, Scale, Str, CStrLen(Str));
}

Void RenderBufDrawRect(RenderBuf *RenderBuf, TexHandle Tex, Rect Dst, Rect Src, Color Color)
{
    RenderDrawRect *Cmd = (RenderDrawRect *)RenderBufPush(RenderBuf, sizeof(RenderDrawRect));

    if (Cmd)
    {
        Cmd->Header.Type = RenderCommand_DrawRect;
        Cmd->Header.Size = BufAlignUp(sizeof(RenderDrawRect));

        Cmd->Tex = Tex;
        Cmd->Dst = Dst;
        Cmd->Src = Src;
        Cmd->Color = Color;
    }
}

Void RenderBufReset(RenderBuf *RenderBuf)
{
    RenderBuf->Size = 0;
}

